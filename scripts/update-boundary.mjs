import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const endpoint =
  "https://nominatim.openstreetmap.org/search?q=Wieringen%2C%20Nederland&format=jsonv2&polygon_geojson=1&limit=1";

const response = await fetch(endpoint, {
  headers: { "User-Agent": "SammeltjesWieringenDev/1.0" }
});

if (!response.ok) {
  throw new Error(`Kon Wieringen-contour niet laden (${response.status}).`);
}

const [result] = await response.json();
if (!result?.geojson || result.geojson.type !== "Polygon") {
  throw new Error("Nominatim gaf geen bruikbare Wieringen-polygoon terug.");
}

const ring = simplify(result.geojson.coordinates[0], 0.001).map(([lng, lat]) => ({
  lat: Number(lat.toFixed(6)),
  lng: Number(lng.toFixed(6))
}));
const [south, north, west, east] = result.boundingbox.map(Number);

const output = `// Generated by scripts/update-boundary.mjs from OpenStreetMap relation ${result.osm_id}.
window.SAMMELTJES_SHARED_CONFIG = Object.freeze({
  WIERINGEN_POLYGON: ${JSON.stringify(ring, null, 2)},
  WIERINGEN_BOUNDS: Object.freeze({
    south: ${south},
    west: ${west},
    north: ${north},
    east: ${east}
  }),
  ADMIN_MARGIN_METERS: 2000,
  GAME_MARGIN_METERS: 1800
});
`;

await writeFile(path.join(root, "shared-config.js"), output, "utf8");
console.log(`shared-config.js bijgewerkt met ${ring.length} contourpunten.`);

function simplify(points, tolerance) {
  if (points.length < 3) {
    return points;
  }

  let furthestIndex = 0;
  let furthestDistance = 0;

  for (let index = 1; index < points.length - 1; index += 1) {
    const distance = distanceToSegment(points[index], points[0], points.at(-1));
    if (distance > furthestDistance) {
      furthestDistance = distance;
      furthestIndex = index;
    }
  }

  if (furthestDistance <= tolerance) {
    return [points[0], points.at(-1)];
  }

  const left = simplify(points.slice(0, furthestIndex + 1), tolerance);
  const right = simplify(points.slice(furthestIndex), tolerance);
  return left.slice(0, -1).concat(right);
}

function distanceToSegment(point, start, end) {
  const deltaX = end[0] - start[0];
  const deltaY = end[1] - start[1];
  if (deltaX === 0 && deltaY === 0) {
    return Math.hypot(point[0] - start[0], point[1] - start[1]);
  }

  const factor = Math.max(
    0,
    Math.min(
      1,
      ((point[0] - start[0]) * deltaX + (point[1] - start[1]) * deltaY) /
        (deltaX * deltaX + deltaY * deltaY)
    )
  );

  return Math.hypot(
    point[0] - (start[0] + factor * deltaX),
    point[1] - (start[1] + factor * deltaY)
  );
}
